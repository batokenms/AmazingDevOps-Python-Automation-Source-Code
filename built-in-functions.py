# abs(): Returns the absolute value of a number.
num = -5
abs_num = abs(num)
print(abs_num)  # Output: 5

# round(): Rounds a number to a specified number of decimal places.
pi = 3.14159
rounded_pi = round(pi, 2)
print(rounded_pi)  # Output: 3.14

# min(): Returns the smallest value among the given arguments.

nums = [5, 2, 8, 3, 1]
smallest_num = min(nums)
print(smallest_num)  # Output: 1

# max(): Returns the largest value among the given arguments.

nums = [5, 2, 8, 3, 1]
largest_num = max(nums)
print(largest_num)  # Output: 8

# sum(): Calculates the sum of all elements in an iterable.

nums = [1, 2, 3, 4, 5]
sum_of_nums = sum(nums)
print(sum_of_nums)  # Output: 15

# pow(): Raises a number to a specified power.

base = 2
exponent = 3
result = pow(base, exponent)
print(result)  # Output: 8
